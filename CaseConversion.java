package stringHandling;

public class CaseConversion {

	public static void main(String[] args) {
		String s1="SACHIN";
		String s2="powar";
		System.out.println("Given String \ns1 : "+s1+"\ns2 : "+s2);
		System.out.println("Convert into LowerCase Using s1.toLowerCase() : \n"+s1.toLowerCase());
		System.out.println("Convert into UpperCase Using s2.toUpperCase() : \n"+s2.toUpperCase());

	}

}
/*OUTPUT
Given String 
s1 : SACHIN
s2 : powar
Convert into LowerCase Using s1.toLowerCase() : 
sachin
Convert into UpperCase Using s2.toUpperCase() : 
POWAR
*/